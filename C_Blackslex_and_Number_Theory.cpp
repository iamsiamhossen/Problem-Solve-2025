void solve()
{
    int n;
    cin >> n;
    vector<int> arr(n);
    for (int i = 0; i < n; i++)
    {
        cin >> arr[i];
    }
    sort(all(arr));
    int x1 = arr[0];
    int x2 = arr[1];
    int dis = x2 - x1;
    if (dis >= x1)
    {
        cout << dis << endl;
    }
    else
    {
        cout << x1 << endl;
    }
}
