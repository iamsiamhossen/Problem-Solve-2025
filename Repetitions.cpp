void solve()
{
    string s;
    cin >> s;
    int cnt = 0, ch = 0;
    int i = 0, j = 1;
    while (j <= s.size())
    {
        if (s[i] == s[j])
        {
            ch++;
            j++;
        }
        else
        {
            i = j;
            j++;
            cnt = max(cnt, ch + 1);
            ch = 0;
        }
    }
    cout << cnt << endl;
}
