void solve()
{
    int n, m;
    cin >> n >> m;
    vector<int> rows(n, 0), cols(m, 0);
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
        {
            int x;
            cin >> x;
            rows[i] ^= x;
            cols[j] ^= x;
        }
    }
    int rowsxor = 0, colxor = 0, total = 0;
    rowsxor = accumulate(all(rows), 0ll);
    colxor = accumulate(all(cols), 0ll);
    total = rowsxor + colxor;
    // cout << total << endl;
}
