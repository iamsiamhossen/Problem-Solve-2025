void solve()
{
    int n, m;
    cin >> n >> m;
    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }
    for (int i = 0; i < n; i++)
    {
        bool flag = true;
        for (int j = 0; j < n; j++)
        {
            if (i != j)
            {
                int t = abs(a[i] - a[j]);
                if (t % m == 0)
                {
                    flag = false;
                }
            }
        }
        if(flag)
        {
            YES;
            cout << i+1 << endl;
            return;
        }
    }
    NO;
}
